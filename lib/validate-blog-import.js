// Runs in both client and server contexts — no 'server-only' import.

const VALID_STATUSES = new Set(['draft', 'published', 'archived']);

function toSlug(str) {
  return String(str)
    .toLowerCase()
    .replace(/ä/g, 'ae').replace(/ö/g, 'oe').replace(/ü/g, 'ue').replace(/ß/g, 'ss')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
}

function isString(v) { return typeof v === 'string'; }
function isObject(v) { return v !== null && typeof v === 'object' && !Array.isArray(v); }
function str(v, fallback = '') { return isString(v) ? v.trim() : fallback; }

// Accepts both camelCase (Claude output) and snake_case keys.
function pick(data, ...keys) {
  for (const k of keys) {
    if (data[k] !== undefined && data[k] !== null) return data[k];
  }
  return undefined;
}

/**
 * Parse and validate a blog article JSON (string or object).
 *
 * Returns:
 *   { valid: true,  errors: [], warnings: [], autogenerated: string[], normalized: {...} }
 *   { valid: false, errors: [], warnings: [], autogenerated: [],        normalized: null  }
 *
 * The `normalized` shape matches the `f` state in BlogEditorClient so it can
 * be merged directly:  setF(prev => ({ ...prev, ...normalized }))
 */
export function validateBlogImport(raw) {
  // ── 1. Parse ─────────────────────────────────────────────────────────────
  let data;
  if (isString(raw)) {
    const trimmed = raw.trim();
    if (!trimmed) return make(false, ['Kein JSON eingegeben.']);
    try { data = JSON.parse(trimmed); }
    catch (e) { return make(false, [`Ungültiges JSON: ${e.message}`]); }
  } else if (isObject(raw)) {
    data = raw;
  } else {
    return make(false, ['Eingabe muss ein JSON-Objekt sein.']);
  }

  if (!isObject(data)) return make(false, ['JSON muss ein Objekt sein, kein Array oder Primitiv.']);

  const errors       = [];
  const warnings     = [];
  const autogenerated = [];

  // ── 2. Required: title ───────────────────────────────────────────────────
  const title = str(pick(data, 'title'));
  if (!title) errors.push('Pflichtfeld fehlt: title');

  // ── 3. Slug (auto-generate from title if missing) ────────────────────────
  let slug = str(pick(data, 'slug')).replace(/\s+/g, '-').toLowerCase();
  if (!slug && title) {
    slug = toSlug(title);
    autogenerated.push('slug');
    warnings.push(`Slug automatisch generiert: "${slug}"`);
  }
  if (!slug) errors.push('Pflichtfeld fehlt: slug (und kein title für Auto-Generierung)');

  // ── 4. Status ─────────────────────────────────────────────────────────────
  let status = str(pick(data, 'status'), 'draft').toLowerCase();
  if (!VALID_STATUSES.has(status)) {
    warnings.push(`Ungültiger status "${data.status}" → "draft" gesetzt.`);
    status = 'draft';
  }

  // ── 5. Simple string fields ───────────────────────────────────────────────
  const excerpt      = str(pick(data, 'excerpt'));
  const category     = str(pick(data, 'category'));
  const author       = str(pick(data, 'author'));
  const readingTime  = str(pick(data, 'readingTime', 'reading_time'));
  const date         = str(pick(data, 'date', 'publishedAt', 'published_at')).replace('T', '').slice(0, 10);
  const destination  = str(pick(data, 'destination'));
  const country      = str(pick(data, 'country'));
  const seoTitle     = str(pick(data, 'seoTitle', 'seo_title'));
  const seoDesc      = str(pick(data, 'seoDescription', 'seo_description'));
  const canonicalUrl = str(pick(data, 'canonicalUrl', 'canonical_url'));

  // ── 6. Images (optional – carry over from JSON if present) ───────────────
  const coverImageUrl = str(pick(data, 'coverImageUrl', 'cover_image_url'));
  const heroImageUrl  = str(pick(data, 'heroImageUrl', 'hero_image_url'));

  // ── 7. Boolean ───────────────────────────────────────────────────────────
  const featured = pick(data, 'featured') === true;

  // ── 8. Tags → comma-separated string for form field ──────────────────────
  const rawTags = pick(data, 'tags');
  let tags = '';
  if (Array.isArray(rawTags)) {
    tags = rawTags.filter(t => isString(t)).map(t => t.trim()).filter(Boolean).join(', ');
  } else if (isString(rawTags)) {
    tags = rawTags.trim();
  }

  // ── 9. tableOfContents ───────────────────────────────────────────────────
  const rawToc = pick(data, 'tableOfContents', 'table_of_contents');
  let tableOfContents = [];
  if (Array.isArray(rawToc)) {
    tableOfContents = rawToc.filter(item => isObject(item) || isString(item));
    if (!tableOfContents.length && rawToc.length) {
      warnings.push('tableOfContents-Einträge haben unbekanntes Format – werden ignoriert.');
    }
  } else if (rawToc != null) {
    warnings.push('tableOfContents ist kein Array – wird ignoriert.');
  }

  // ── 10. contentSections ──────────────────────────────────────────────────
  const rawSections = pick(data, 'contentSections', 'content_sections', 'content');
  let contentSections = [];
  if (Array.isArray(rawSections)) {
    contentSections = rawSections;
  } else if (isString(rawSections) && rawSections) {
    // If someone passes a plain HTML/markdown string, wrap it in one section.
    contentSections = [{ id: 'inhalt', title: 'Inhalt', content: rawSections }];
    autogenerated.push('contentSections');
    warnings.push('content war ein String – wurde als einzelner contentSection-Eintrag gewrappt.');
  } else if (rawSections != null) {
    warnings.push('contentSections ist kein Array – wird ignoriert.');
  }

  // ── 11. faq ──────────────────────────────────────────────────────────────
  const rawFaq = pick(data, 'faq');
  let faq = [];
  if (Array.isArray(rawFaq)) {
    faq = rawFaq.filter(item => isObject(item) && (item.question || item.answer));
    if (!faq.length && rawFaq.length) {
      warnings.push('faq-Einträge fehlen "question"/"answer" – werden ignoriert.');
    }
  } else if (rawFaq != null) {
    warnings.push('faq ist kein Array – wird ignoriert.');
  }

  // ── 12. relatedArticles ──────────────────────────────────────────────────
  const rawRel = pick(data, 'relatedDestinations', 'relatedArticles', 'related_articles');
  const relatedArticles = Array.isArray(rawRel) ? rawRel.filter(s => isString(s)) : [];

  // ── 13. keyTakeaways ─────────────────────────────────────────────────────
  const rawKt = pick(data, 'keyTakeaways', 'key_takeaways');
  const keyTakeaways = Array.isArray(rawKt) ? rawKt : [];

  // ── 14. galleryImages ────────────────────────────────────────────────────
  const rawGallery = pick(data, 'galleryImages', 'gallery_images');
  let galleryImages = [];
  if (Array.isArray(rawGallery)) {
    galleryImages = rawGallery
      .filter(item => (isObject(item) && item.url) || isString(item))
      .map((item, i) =>
        isString(item)
          ? { url: item, alt: '', title: '', caption: '', order: i }
          : { url: item.url ?? '', alt: item.alt ?? '', title: item.title ?? '', caption: item.caption ?? '', order: i }
      );
  } else if (rawGallery != null) {
    warnings.push('galleryImages ist kein Array – wird ignoriert.');
  }

  // ── 15. SEO warnings ─────────────────────────────────────────────────────
  if (!seoTitle)    warnings.push('seoTitle fehlt – Artikel-Titel wird als Fallback verwendet.');
  if (!seoDesc)     warnings.push('seoDescription fehlt – für SEO empfohlen.');
  if (!excerpt)     warnings.push('excerpt fehlt – für Listen und Social Sharing empfohlen.');
  if (!category)    warnings.push('category fehlt.');
  if (!destination) warnings.push('destination fehlt.');
  if (!contentSections.length) warnings.push('contentSections ist leer – Artikel hat keinen Inhalt.');
  if (!tableOfContents.length) warnings.push('tableOfContents ist leer.');
  if (!faq.length)  warnings.push('faq ist leer.');

  // ── 15. Bail if hard errors ───────────────────────────────────────────────
  if (errors.length > 0) return make(false, errors, warnings);

  // ── 16. normalized → matches BlogEditorClient `f` state ──────────────────
  const normalized = {
    title,
    slug,
    excerpt,
    category,
    tags,
    author,
    reading_time:    readingTime,
    date,
    destination,
    country,
    featured,
    cover_image_url: coverImageUrl,
    hero_image_url:  heroImageUrl,
    seo_title:       seoTitle,
    seo_description: seoDesc,
    canonical_url:   canonicalUrl,
    status,
    // JSON arrays – kept as objects; caller serialises to string for form fields
    _tableOfContents: tableOfContents,
    _contentSections: contentSections,
    _faq:             faq,
    _relatedArticles: relatedArticles,
    _keyTakeaways:    keyTakeaways,
    _galleryImages:   galleryImages,
  };

  return { valid: true, errors: [], warnings, autogenerated, normalized };
}

function make(valid, errors, warnings = []) {
  return { valid, errors, warnings, autogenerated: [], normalized: null };
}
